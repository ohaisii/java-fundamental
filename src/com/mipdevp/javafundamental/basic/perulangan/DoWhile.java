package com.mipdevp.javafundamental.basic.perulangan;

public class DoWhile {

    public static void main(String[] args) {
        System.out.println("Perulangan do while");
        int i = 1;
        do {
            System.out.println("Perulangan dengan do ke - " + i);
            i++;
        }
        while(i < 10);
    }
}

