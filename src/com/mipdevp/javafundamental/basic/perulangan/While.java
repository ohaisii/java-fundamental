package com.mipdevp.javafundamental.basic.perulangan;

public class While {

    public static void main(String[] args) {

        System.out.println("Perulangan while");
        int i = 1;
        while (i < 10) {

            System.out.println("Perulangan while ke - " + i);
            i++;
        }
    }
}
