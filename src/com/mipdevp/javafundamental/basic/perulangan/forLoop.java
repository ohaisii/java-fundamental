package com.mipdevp.javafundamental.basic.perulangan;

public class forLoop {

    public static void main(String[] args) {

        int i; //Mendefinisikan variabel dan tipe data
        //sintak dasar perulangan, hampir sama seperti bahasa pemrograman lainnya
        for(i = 1; i <= 5; i++) {
            //akan mencetak tulisan di bawah ini sebanyak 5 kali
            System.out.println("Ini adalah perulangan ke - " + i);
        }
    }
}
