package com.mipdevp.javafundamental.basic.tipedata;

public class Primitif {

    public static void main(String[] args) {

        System.out.println("Tipe Data Primitif di Java adalah tipe data standar yang tidak diturunkan dari objek manapun");

        System.out.println("Tipe Data Numerik");
        byte value = -5;
        short anotherValue = 1000;
        int bilBulat = 10;
        long bilMilyaran = 150000L;
        float bilFlout = 2.0f;
        double bilDouble = 5.0d;
        System.out.println(value);
        System.out.println(anotherValue);
        System.out.println(bilBulat);
        System.out.println(bilMilyaran);
        System.out.println(bilDouble);
        System.out.println(bilFlout);

        System.out.println("Tipe Data Logika");
        System.out.println("True and False");
        boolean x = true;
        boolean z = false;
        System.out.println(x);
        System.out.println(z);

        System.out.println("Tipe Data Karakter");
        char item = 'A';
        System.out.println(item);


    }

}
